// Кнопка паузы видео
let isPaused = false;
toggleBtn.addEventListener("click", ()=>{
    if(!isPaused){
        video.pause();
        bgImage.style.opacity = 1;
        toggleBtn.textContent = "▶";
    } else {
        video.play();
        bgImage.style.opacity = 0;
        toggleBtn.textContent = "⏸";
    }
    isPaused = !isPaused;
});
